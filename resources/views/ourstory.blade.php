        <h1>Our Story</h1>
        <p>
            BYSMP Bless the Children Foundation (BCFI) was established to give identity to the growing mission of 
            San Martin de Porres Manila. Separate but not different from its mother’s mission, Bahay at Yaman ni 
            San Martin de Porres, Inc. (BYSMP), the organization is known as “St. Martin – Bless the Children,” 
            a prayerful invocation to our Patron Saint, St. Martin de Porres.
        </p>
        <div class="timeline">
            <div class="timeline-item">
                <h3>2002</h3>
                <p>
                    Since the launch of the Manila Mission of St. Martin in 2002, its presence has been felt in the disadvantaged barangays of Binondo and Tondo through various programs, including feeding programs, free medical clinics, tutorials, and values formation.
                </p>
            </div>
            <div class="timeline-item">
                <h3>2009</h3>
                <p>
                    The Foundation began developing programs in Tondo, Manila, responding to growing poverty. 
                    A partnership with Ancop introduced holistic programs to assist children and families.
                </p>
            </div>
            <div class="timeline-item">
                <h3>2013</h3>
                <p>
                    On July 1, 2013, a lease agreement allowed the use of Centro Salvador Building for free. 
                    BCFI partnered with Chalice Canada, launching a sponsorship program that started with 50 children.
                </p>
            </div>
            <div class="timeline-item">
                <h3>2015</h3>
                <p>
                    Chalice Canada established the Tondo Subsite as an independent sponsorship site, enabling 
                    more children and families to be supported.
                </p>
            </div>
            <div class="timeline-item">
                <h3>2020</h3>
                <p>
                    During the COVID-19 pandemic, BCFI expanded its nutrition program to Baseco Compound, 
                    supporting hundreds of children at risk of malnourishment.
                </p>
            </div>
            <div class="timeline-item">
                <h3>2022</h3>
                <p>
                    On June 23, 2022, the Baseco Subsite was inaugurated, expanding operations to assist more children 
                    and families with programs focused on health, nutrition, and education.
                </p>
            </div>
        </div>
        <p>
            Currently, the Foundation continues to reach out to communities on a larger scale with the support of partners and donors. At the end of 2022, BCFI supported over 2,300 children from 1,600 families, inspiring change and hope because every child is worthy.
        </p>
